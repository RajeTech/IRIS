package com.iris.ramilton.iris.modelo;

public class Usuario {

    private int id;
    private String dsMatricula;
    private String dsNome;
    private String dsSenha;
    private String dsUnidade;
    private int dsIdUnidade;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDsMatricula() {
        return dsMatricula;
    }

    public void setDsMatricula(String dsMatricula) {
        this.dsMatricula = dsMatricula;
    }

    public String getDsNome() {
        return dsNome;
    }

    public void setDsNome(String dsNome) {
        this.dsNome = dsNome;
    }

    public String getDsSenha() {
        return dsSenha;
    }

    public void setDsSenha(String dsSenha) {
        this.dsSenha = dsSenha;
    }

    public String getDsUnidade() {
        return dsUnidade;
    }

    public void setDsUnidade(String dsUnidade) {
        this.dsUnidade = dsUnidade;
    }

    public int getDsIdUnidade() {
        return dsIdUnidade;
    }

    public void setDsIdUnidade(int dsIdUnidade) {
        this.dsIdUnidade = dsIdUnidade;
    }




}
